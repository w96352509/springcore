package com.study.springcore.case01;

public class HD {
	private Integer HD;

	public HD(Integer hD) {
		super();
		HD = hD;
	}

	public HD() {

	}

	public Integer getHD() {
		return HD;
	}

	public void setHD(Integer hD) {
		HD = hD;
	}

	@Override
	public String toString() {
		return "HD [HD=" + HD + "]";
	}

}
